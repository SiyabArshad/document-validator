const mongoose = require("mongoose");
const crypto = require("crypto-js");

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  dateOfBirth: {
    type: Date,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});


const User = mongoose.model("User", userSchema);

module.exports = User;
